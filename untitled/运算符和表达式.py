# 判断输入的整数是否为闰年？
# 能被4整除 但是不能被100整除  or  能被400整除的
year = int(input("请输入一个年份:"))
print(year)
if (year % 400 == 0) or (year % 4 == 0 and year % 100 != 0):
    print("是闰年")
else:
    print("不是闰年")
