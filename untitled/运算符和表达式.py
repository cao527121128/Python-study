# 关系运算符
'''
关系运算符：
== != >= <= > <
关系运算表达式：
1 + 2 > 3 + 4
格式：表达式1 关系运算符 表达式2
功能：计算表达式1 和表达式2的值
值：如果关系成立，整个关系运算表达式的值为真，否则为假
'''
print( 1 + 2 < 3 + 4)

# 逻辑运算符 and
'''
逻辑与运算表达式： 表达式1 and 表达式2
值：如果有一个表达式为假，则为假。 全部都为真才为真
'''
num1 = 10
num2 = 20
if num1 and num2:
    print("****************")
print(num1,num2)

# 短路原则
# 表达式1 and 表达式2 and 表达式3...
# 只有有一个为假，则逻辑与运算表达式整体就为假

# 逻辑或运算符 or
'''
逻辑或运算表达式： 表达式1 or 表达式2 
值：有一个为真，则为真
'''
num3 = 0
num4 = 1
if num3 or num4:
    print("&&&&&&&&&&")
print(num3,num4)

#短路原则
# 表达式1 or  表达式2 or 表达式3 or 表达式4...
# 只有有一个为真，该逻辑或运算表达式整体就为真

